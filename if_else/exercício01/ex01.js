const letra = prompt("Digite uma letra qualquer: ");

if (letra === "a" || letra === "e" || letra === "i" || letra === "o" || letra === "u") {
    alert("A letra digitada é uma vogal");
} else {
    alert("A letra digitada é uma consoante");
}

